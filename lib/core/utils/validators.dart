class Validators {
  static String? email(String? v) {
    if (v == null || v.isEmpty) return 'Email required';
    if (!v.contains('@')) return 'Invalid email';
    return null;
  }

  static String? password(String? v) {
    if (v == null || v.length < 6) return 'Min 6 characters';
    return null;
  }
}
